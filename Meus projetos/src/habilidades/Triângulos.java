package habilidades;

public class Triângulos {
	public static void main(String[] args) {
		
		System.out.println("Abordagem do cálculo da área de um triângulo!");
		
		double base = 10;
		double altura = 15;
		double AreaTriangulo = base * altura / 2;
		
		System.out.println("A área do triângulo é: " + AreaTriangulo + "M²");
	}

}
